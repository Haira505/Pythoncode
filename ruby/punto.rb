class Punto
    #constructor
    def initialize(valorx,valory)
        #atributos
        @x = valorx
        @y= valory
    end

    def getx()
        return @x
    end

    def gety()
        return @y
    end

    def setx(valorx)
        @x = valorx
    end

    def sety(valory)
        @y = valory
    end

end



